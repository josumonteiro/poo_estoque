    private void jButtonDesativarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDesativarProdutoActionPerformed
        ImplServicoVeiculos servico = new ImplServicoVeiculos();
        if (jTableListaProduto.getSelectedRow() >= 0) {

            //Obtém a linha do item selecionado
            final int row = jTableListaProduto.getSelectedRow();
            //Obtém a descrição do produto da linha indicada para exibição;
            //de mensagem de confirmação de exclusão utilizando sua descrição.
            String prod = (String) jTableListaProduto.getValueAt(row, 1);
            //Mostra o diálogo de confirmação de exclusão
            int resposta = JOptionPane.showConfirmDialog(rootPane,
                    "Excluir o produto \"" + prod + "\"?",
                    "Confirmar exclusão", JOptionPane.YES_NO_OPTION);
            //Se o valor de resposta for "Sim" para a exclusão
            if (resposta == JOptionPane.YES_OPTION) {

                try {
                    //Obtém o ID do produto
                    Integer idProd = (Integer) jTableListaProduto.getValueAt(row, 0);
                    //Solicita ao serviço a inativação do produto com o ID
                    //ServicoMotos.excluirProduto(idProd);
                    servico.Excluir(idProd, 1);

                    JOptionPane.showMessageDialog(this, "Produto excluído com sucesso.");
                    //Atualiza a lista após a "exclusão"
                    this.refreshList();
                } catch (Exception e) {

                    e.printStackTrace();
                    //Exibe uma mensagem de erro genérica ao usuário
                    JOptionPane.showMessageDialog(rootPane, e.getMessage(),
                            "Falha na Exclusão", JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione o produto que deseja excluir.");
        }
    }//GEN-LAST:event_jButtonDesativarProdutoActionPerformed

