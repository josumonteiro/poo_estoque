    public boolean refreshList() throws Exception {
        ImplServicoVeiculos servico = new ImplServicoVeiculos();
        //List<Moto> resultado = ServicoMotos.procurarProduto(jTextFieldModelo.getText());
        List<Object> resultado = servico.Procurar(jTextFieldModelo.getText(), 1);

        DefaultTableModel model = (DefaultTableModel) jTableListaProduto.getModel();

        model.setRowCount(0);

        if (resultado == null || resultado.size() <= 0) {
            return false;
        }

        int alertaEstoq = 6;
        String desc = null;

        for (int i = 0; i < resultado.size(); i++) {
            Moto produto = (Moto) resultado.get(i);
            if (produto != null) {
                Object[] row = new Object[9];
                row[0] = produto.getId();
                row[1] = produto.getMarca();
                row[2] = produto.getModelo();
                row[3] = produto.getCor();
                row[4] = produto.getAno();
                row[5] = produto.getAlturaAssento();
                row[6] = produto.getQtd();
                row[7] = produto.getPreco();
                model.addRow(row);
            }
        }
        return true;
    }

